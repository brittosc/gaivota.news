export function generateRandomAuthor(): string {
  const firstName = firstNames[Math.floor(Math.random() * firstNames.length)];
  const lastName = lastNames[Math.floor(Math.random() * lastNames.length)];

  // 1 in 30 chance to have a second last name
  const hasSecondLastName = Math.random() < 1 / 30;

  if (hasSecondLastName) {
    const secondLastName = lastNames[Math.floor(Math.random() * lastNames.length)];
    return `${firstName} ${lastName} ${secondLastName}`;
  }

  return `${firstName} ${lastName}`;
}
